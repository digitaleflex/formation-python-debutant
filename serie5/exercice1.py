def carre(nombre: float) -> float:
    """
    Calcule le carré d'un nombre.

    Args:
        nombre (float): Le nombre dont on veut calculer le carré.

    Returns:
        float: Le carré du nombre.
    """
    return nombre ** 2

# Exemple d'utilisation
if __name__ == "__main__":
    print(carre(5))  # Résultat : 25